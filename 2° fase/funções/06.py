# Crie um script em linguagem Python com uma função que recebe como entrada uma lista
# de números e retorna True se um número passado como parâmetro está presente na lista.

geral = [ ]
def sla():
    a = int(input('informe um numero:'))
    geral.append(a)
    b = int(input("informe um limete"))
    for v in geral:
        if b == v:
            return True
        else:
            print(sla())
        
print(sla())